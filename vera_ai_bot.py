class LLMClient:
    """
    ---------------------------------------------------------
    Gemini LLM Client
    ---------------------------------------------------------

    Responsible for communicating with Gemini.

    This class NEVER:

    - Reads datasets
    - Makes business decisions
    - Builds prompts
    - Validates responses

    It only sends PromptBundle to Gemini
    and returns an LLMResponse.
    """

    def __init__(self):
        """
        Initialize Gemini SDK client.
        """

        self.model = MODEL_NAME

        self.client = None

        if GEMINI_API_KEY:
            self.client = genai.Client(
                api_key=GEMINI_API_KEY
            )

    # ------------------------------------------------------
    # PUBLIC METHOD
    # ------------------------------------------------------

    def generate(
        self,
        prompt_bundle: PromptBundle
    ) -> LLMResponse:
        """
        Generate an AI response from a PromptBundle.

        This method orchestrates the complete
        LLM pipeline.
        """

        try:
            if DEBUG:
                print("=" * 80)
                ("SYSTEM PROMPT")
                print("=" * 80)
                print(prompt_bundle.system_prompt)

                print("-" * 80)

                print("USER PROMPT")
                print("=" * 80)
                print(prompt_bundle.user_prompt)

                print("=" * 80)

            raw_response = self._call_gemini(
                prompt_bundle
            )

            return self._parse_response(
                raw_response
            )

        except Exception as error:

            print(
                f"LLM ERROR: {error}"
            )

            return self._fallback_response()
        

    # ------------------------------------------------------
    # HELPER 1
    # CALL GEMINI
    # ------------------------------------------------------

    def _call_gemini(
        self,
        prompt_bundle: PromptBundle
    ):
        """
        Send prompts to Gemini and return
        the raw SDK response.
        """

        if self.client is None:

            raise RuntimeError(
                "Gemini client is not initialized."
            )

        response = self.client.models.generate_content(

            model=self.model,

            contents=prompt_bundle.user_prompt,

            config={

                "system_instruction": prompt_bundle.system_prompt,

                "temperature": 0.4,

                "response_mime_type": "application/json"

            }

        )

        if DEBUG:
            print("RAW GEMINI RESPONSE")
            print(response.text)


        if not response.text:
            raise RuntimeError(
                "Gemini returned an empty response."
                )

        return response         


    # ------------------------------------------------------
    # HELPER 2
    # PARSE GEMINI RESPONSE
    # ------------------------------------------------------

    def _parse_response(
        self,
        raw_response
    ) -> LLMResponse:
        """
        Parse Gemini response into a validated
        LLMResponse object.
        """

        try:

            # Clean response text before parsing
            text = (raw_response.text or "").strip()

            # Remove Markdown JSON fences if the model returns them

            if text.startswith("```json"):
                text = text[7:]

            if text.startswith("```"):
                text = text[3:]

            if text.endswith("```"):
                text = text[:-3]

            text = text.strip()
            
            response_json = json.loads(text)


            return LLMResponse(

                body=response_json.get(
                    "body",
                    ""
                ),

                cta=response_json.get(
                    "cta",
                    ""
                ),

                send_as=response_json.get(
                    "send_as",
                    "whatsapp"
                ),

                metadata=response_json.get(
                    "metadata",
                    {}
                )

            )

        except Exception as error:

            print(
                f"JSON Parse Error: {error}"
            )

            return self._fallback_response()

    # ------------------------------------------------------
    # HELPER 3
    # FALLBACK RESPONSE
    # ------------------------------------------------------

    def _fallback_response(
        self
    ) -> LLMResponse:
        """
        Safe response returned whenever
        Gemini fails.
        """

        return LLMResponse(

            body=(
                "Unable to generate a response "
                "at this time."
            ),

            cta="Learn More",

            send_as="whatsapp",

            metadata={

                "fallback": True

            }

        )
    

