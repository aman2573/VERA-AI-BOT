@app.post(
    "/tick",
    response_model=TickResponse
)
def tick(
    request: TickRequest
) -> TickResponse:
    """
    Main Judge endpoint.

    Receives a Trigger ID and
    returns the actions Vera wants
    to perform.
    """

    # --------------------------------------------------
    # Build Context
    # --------------------------------------------------

    context = store.build_context(

        request.trigger_id

    )

    # --------------------------------------------------
    # Evaluate Trigger
    # --------------------------------------------------

    decision = trigger_evaluator.evaluate(

        context

    )

    # --------------------------------------------------
    # Skip Trigger
    # --------------------------------------------------

    if not decision.should_continue:

        return TickResponse(

            actions=[]

        )
    
    # --------------------------------------------------
    # Decision Engine
    # --------------------------------------------------

    plan = decision_engine.decide(

        context,

        decision

    )

    # --------------------------------------------------
    # Prompt Composer
    # --------------------------------------------------

    prompt = prompt_composer.compose(

        context,

        plan

    )

    # --------------------------------------------------
    # LLM Client
    # --------------------------------------------------

    response = llm_client.generate(

        prompt

    )

    # --------------------------------------------------
    # Validate Response
    # --------------------------------------------------

    response = action_validator.validate(

        plan,

        response

    )

    # --------------------------------------------------
    # Build Final Action
    # --------------------------------------------------

    action = reply_engine.build_action(

        plan,

        response

    )

    # --------------------------------------------------
    # Return Response
    # --------------------------------------------------

    return TickResponse(

        actions=[

            action

        ]

    )

