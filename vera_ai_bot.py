class ActionValidator:
    """
    ---------------------------------------------------------
    Action Validator
    ---------------------------------------------------------

    Responsible for validating AI responses before
    they leave the application.

    This class NEVER:

    - Calls the LLM
    - Reads datasets
    - Makes business decisions

    It only validates an LLMResponse.
    """

    MAX_BODY_LENGTH = 1200

    MAX_CTA_LENGTH = 80

    ALLOWED_CHANNELS = {
        "whatsapp",
        "sms",
        "push"
    }

    def __init__(self):
        """Initialize validator."""
        pass

    # ------------------------------------------------------
    # PUBLIC METHOD
    # ------------------------------------------------------

    def validate(
        self,
        plan: ExecutionPlan,
        response: LLMResponse
    ) -> LLMResponse:
        """
        Validate the generated response.

        Returns a cleaned and validated response.
        """

        self._validate_body(response)

        self._validate_cta(response)

        self._validate_channel(response)

        self._validate_metadata(
            plan,
            response
        )

        return response


    # ------------------------------------------------------
    # HELPER 1
    # VALIDATE BODY
    # ------------------------------------------------------

    def _validate_body(
        self,
        response: LLMResponse
    ) -> None:
        """
        Validate and repair the message body.
        """

        # Remove leading/trailing whitespace
        response.body = response.body.strip()

        # If body is empty, provide a safe fallback
        if not response.body:

            response.body = (
                "We have an update that may be relevant to your business."
            )

        # Prevent excessively long responses
        MAX_BODY_LENGTH = self.MAX_BODY_LENGTH

        if len(response.body) > MAX_BODY_LENGTH:

            response.body = (
                response.body[:MAX_BODY_LENGTH].rstrip()
                + "..."
            )


    # ------------------------------------------------------
    # HELPER 2
    # VALIDATE CTA
    # ------------------------------------------------------

    def _validate_cta(
        self,
        response: LLMResponse
    ) -> None:
        """
        Validate and repair the CTA.
        """

        response.cta = response.cta.strip()

        # Default CTA if missing
        if not response.cta:

            response.cta = "Learn More"

        MAX_CTA_LENGTH = self.MAX_CTA_LENGTH

        if len(response.cta) > MAX_CTA_LENGTH:

            response.cta = (
                response.cta[:MAX_CTA_LENGTH].rstrip()
            )


    # ------------------------------------------------------
    # HELPER 3
    # VALIDATE CHANNEL
    # ------------------------------------------------------

    def _validate_channel(
        self,
        response: LLMResponse
    ) -> None:
        """
        Validate and repair the delivery channel.
        """

        response.send_as = response.send_as.strip().lower()

        if response.send_as not in self.ALLOWED_CHANNELS:

            response.send_as = "whatsapp"

    # ------------------------------------------------------
    # HELPER 4
    # VALIDATE METADATA
    # ------------------------------------------------------

    def _validate_metadata(
        self,
        plan: ExecutionPlan,
        response: LLMResponse
    ) -> None:
        """
        Ensure metadata exists and contains
        the required system information.
        """

        if response.metadata is None:

            response.metadata = {}

        response.metadata.setdefault(
            "validated",
            True
        )

        trigger = plan.trigger or {}

        response.metadata.setdefault(
            "trigger_id",
            trigger.get("id")
        )

        response.metadata.setdefault(
            "merchant_id",
            trigger.get("merchant_id")
        )

        response.metadata.setdefault(
            "action_type",
            plan.action_type
        )


