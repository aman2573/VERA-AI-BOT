class StoreManager:
    """
    =========================================================
    Central Memory Manager of Vera AI
    =========================================================

    StoreManager is the single source of truth for the bot.

    It is responsible ONLY for:

    • Loading dataset into memory
    • Maintaining runtime state
    • Fast O(1) lookup
    • Conversation storage
    • Suppression tracking

    It NEVER performs business logic.

    Decision making belongs to:

        TriggerEvaluator
        DecisionEngine
        ReplyEngine

    Those classes will ONLY communicate through StoreManager.
    """

    def __init__(self):
        """
        Constructor.

        At startup we ONLY allocate memory.

        Dataset loading happens separately.
        """

        # --------------------------------------------------
        # STATIC MEMORY
        #
        # Loaded once.
        # Never changes afterwards.
        # --------------------------------------------------

        self._static_data = {

            # category_slug -> category json
            "categories": {},

            # merchant_id -> merchant json
            "merchants": {},

            # customer_id -> customer json
            "customers": {},

            # trigger_id -> trigger json
            "triggers": {}

        }

        # --------------------------------------------------
        # RUNTIME MEMORY
        #
        # Changes while bot is running.
        # --------------------------------------------------

        self._runtime_data = {

            # conversation_id -> conversation object
            "conversations": {},

            # suppression_key -> expiry
            "suppression": {},

            # context_id -> context object
            "contexts": {},

            # frequently reused objects
            "cache": {}

        }

        # --------------------------------------------------
        # INTERNAL STATE
        # --------------------------------------------------

        # Prevent loading dataset twice.
        self._loaded = False

        # Timestamp when dataset was loaded.
        self._loaded_at = None

        
    # ==========================================================
    # DATASET LOADING
    # ==========================================================
    def _load_json(self, file_path: Path) -> Dict[str, Any]:
        """
        Private helper function.

        Reads a JSON file from disk and returns
        the parsed Python object.

        WHY?
        ----
        Instead of writing:

            open(...)
            json.load(...)

        four different times, we centralize
        the logic here.

        Raises:
            FileNotFoundError
            ValueError
        """

        if not file_path.exists():
            raise FileNotFoundError(
                f"Dataset file not found: {file_path}"
            )

        try:
            with open(file_path, "r", encoding="utf-8") as file:
                return json.load(file)

        except json.JSONDecodeError as e:
            raise ValueError(
                f"Invalid JSON format in {file_path}: {e}"
            )


    def load_categories(self):
        """
        Loads every category JSON from

            dataset/categories/

        Example:

            dentists.json
            salons.json
            gyms.json

        Stored as:

            self._static_data["categories"][slug]
        """

        category_store = self._static_data["categories"]

        for category_file in CATEGORY_DIR.glob("*.json"):

            category = self._load_json(category_file)

            slug = category.get("slug")

            if slug:
                category_store[slug] = category


    def load_merchants(self):
        """
        Loads all merchants into memory.

        Key:

            merchant_id

        Lookup Time:

            O(1)
        """

        merchants = self._load_json(MERCHANT_FILE)

        merchant_store = self._static_data["merchants"]

        for merchant in merchants.get("merchants", []):

            merchant_id = merchant.get("merchant_id")
            
            if merchant_id:
                merchant_store[
                merchant["merchant_id"]
                ] = merchant


    def load_customers(self):
        """
        Loads all customers.

        Key:

            customer_id
        """

        customers = self._load_json(CUSTOMER_FILE)

        customer_store = self._static_data["customers"]

        for customer in customers.get("customers", []):

            customer_id = customer.get("customer_id")

            if customer_id:

                customer_store[
                    customer["customer_id"]
                ] = customer


    def load_triggers(self):
        """
        Loads all triggers.

        Key:

            trigger_id
        """

        triggers = self._load_json(TRIGGER_FILE)

        trigger_store = self._static_data["triggers"]

        for trigger in triggers.get("triggers", []):

            trigger_id = trigger.get("id")

            if trigger_id:

                trigger_store[
                    trigger["id"]
                ] = trigger


    def load_all_data(self):
        """
        Master loader.

        Loads every dataset exactly once.

        Safe to call multiple times.
        """

        if self._loaded:
            return

        self.load_categories()
        self.load_merchants()
        self.load_customers()
        self.load_triggers()

        self._loaded = True
        self._loaded_at = datetime.now(timezone.utc)

        if DEBUG:
            print(
                "Datasets loaded successfully."
            )

            print(
                f"Categories : {len(self._static_data['categories'])}"
            )

            print(
                f"Merchants  : {len(self._static_data['merchants'])}"
            )

            print(
                f"Customers  : {len(self._static_data['customers'])}"
            )

            print(
                f"Triggers   : {len(self._static_data['triggers'])}"
            )

    # ==========================================================
    # LOOKUP METHODS
    # ==========================================================

    def get_category(self, category_slug: str) -> Optional[Dict[str, Any]]:
        """
        Returns a category by its slug.

        Example:
            dentists
            gyms
            salons

        Time Complexity:
            O(1)
        """
        return self._static_data["categories"].get(category_slug)


    def get_merchant(self, merchant_id: str) -> Optional[Dict[str, Any]]:
        """
        Returns a merchant by merchant_id.

        Time Complexity:
            O(1)
        """
        return self._static_data["merchants"].get(merchant_id)


    def get_customer(self, customer_id: str) -> Optional[Dict[str, Any]]:
        """
        Returns a customer by customer_id.

        Time Complexity:
            O(1)
        """
        return self._static_data["customers"].get(customer_id)


    def get_trigger(self, trigger_id: str) -> Optional[Dict[str, Any]]:
        """
        Returns a trigger by trigger_id.

        Time Complexity:
            O(1)
        """
        return self._static_data["triggers"].get(trigger_id)
    

    # ==========================================================
    # RUNTIME MEMORY
    # ==========================================================

    def save_context(
        self,
        conversation_id: str,
        context: Dict[str, Any]
    ) -> None:
        """
        Stores runtime context for a conversation.

        Called by:

            POST /context

        If context already exists,
        it is replaced with the newest one.
        """

        self._runtime_data["contexts"][conversation_id] = context


    def get_context(
        self,
        conversation_id: str
    ) -> Optional[Dict[str, Any]]:
        """
        Returns runtime context.

        Returns None if context
        does not exist.
        """

        return self._runtime_data["contexts"].get(
            conversation_id
        )


    def save_conversation(
        self,
        conversation_id: str,
        conversation: Dict[str, Any]
    ) -> None:
        """
        Stores conversation state.

        Example

        history

        intent

        current_state

        last_reply

        etc.
        """

        self._runtime_data["conversations"][
            conversation_id
        ] = conversation


    def get_conversation(
        self,
        conversation_id: str
    ) -> Optional[Dict[str, Any]]:
        """
        Returns stored conversation.

        Returns None if conversation
        does not exist.
        """

        return self._runtime_data[
            "conversations"
        ].get(conversation_id)
    
    # ==========================================================
    # SUPPRESSION
    # ==========================================================

    def suppress(
        self,
        suppression_key: str,
        expires_at: datetime
    ) -> None:
        """
        Stores a suppression entry.

        Once suppressed, the same trigger/message
        should not be generated again until expiry.

        Example:
            "merchant_001:research_digest"
        """

        self._runtime_data["suppression"][suppression_key] = expires_at


    def is_suppressed(
        self,
        suppression_key: str
    ) -> bool:
        """
        Checks whether a suppression entry
        is still active.

        Returns:
            True  -> Still suppressed
            False -> Not suppressed / Expired
        """

        expires_at = self._runtime_data["suppression"].get(
            suppression_key
        )

        if expires_at is None:
            return False

        if datetime.now(timezone.utc) >= expires_at:
            del self._runtime_data["suppression"][suppression_key]
            return False

        return True


    # ==========================================================
    # CONTEXT BUILDER
    # ==========================================================

    def build_context(
        self,
        trigger_id: str
    ) -> BotContext:
        """
        Builds one complete execution context.

        Input:
            Trigger ID

        Output:
            BotContext

        Instead of every class performing multiple
        lookups, StoreManager assembles everything
        into one object.

        Flow

        Trigger
            ↓
        Merchant
            ↓
        Category
            ↓
        Customer (optional)
            ↓
        Runtime Context
            ↓
        Conversation
        """

        # --------------------------------------------------
        # Fetch Trigger
        # --------------------------------------------------

        trigger = self.get_trigger(trigger_id)

        if trigger is None:
            raise ValueError(
                f"Unknown Trigger ID : {trigger_id}"
            )

        # --------------------------------------------------
        # Fetch Merchant
        # --------------------------------------------------

        merchant_id = trigger.get("merchant_id")

        merchant = self.get_merchant(merchant_id)

        if merchant is None:
            raise ValueError(
                f"Unknown Merchant ID : {merchant_id}"
            )

        # --------------------------------------------------
        # Fetch Category
        # --------------------------------------------------

        category_slug = merchant.get("category_slug")

        category = self.get_category(category_slug)

        if category is None:
            raise ValueError(
                f"Unknown Category : {category_slug}"
            )

        # --------------------------------------------------
        # Fetch Customer (optional)
        # --------------------------------------------------

        customer = None

        customer_id = trigger.get("customer_id")

        if customer_id:

            customer = self.get_customer(customer_id)

        # --------------------------------------------------
        # Runtime Context
        # --------------------------------------------------

        # runtime_context is not provided in the challenge dataset
        runtime_context = None

        # --------------------------------------------------
        # Conversation
        # --------------------------------------------------

        # conversation history is not available in current dataset
        conversation = None

        # --------------------------------------------------
        # Build BotContext
        # --------------------------------------------------

        return BotContext(

            trigger=trigger,

            merchant=merchant,

            category=category,

            customer=customer,

            runtime_context=runtime_context,

            conversation=conversation
        )
    
