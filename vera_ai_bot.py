class DecisionEngine:
    """
    ---------------------------------------------------------
    Vera Business Decision Engine
    ---------------------------------------------------------

    Responsible for deciding WHAT Vera should do.

    This class NEVER:

    - Generates prompts
    - Calls the LLM
    - Formats messages
    - Talks to FastAPI

    It only creates an ExecutionPlan.
    """

    LLM_REQUIRED = {

            "research_digest": True,

            "regulation_change": True,

            "perf_dip": True,

            "renewal_due": True,

            "recall_due": False
    }

    def __init__(self):
        """Initializes the Decision Engine."""
        pass


    # ------------------------------------------------------
    # PUBLIC METHOD
    # ------------------------------------------------------

    def decide(
        self,
        context: BotContext,
        decision: TriggerDecision
    ) -> ExecutionPlan:
        """
        Creates an execution plan for a validated trigger.
        """

        if not decision.should_continue:

            return ExecutionPlan(
                action_type="none",
                use_llm=False,
                priority=context.trigger.get("urgency",0),
                trigger=context.trigger,
                payload = context.trigger.get("payload",{}),
                offer = None,
                research = None,
                metadata = {
                    "reason": decision.reason
                }
            )

        action_type = context.trigger.get(
            "kind",
            "unknown"
        )

        offer = self._select_offer(context)

        research = self._select_research(context)

        priority = context.trigger.get("urgency",1)

        use_llm = self._should_use_llm(
            action_type
        )

        return ExecutionPlan(

            action_type=action_type,

            use_llm=use_llm,

            priority=priority,

            trigger = context.trigger,

            payload = context.trigger.get("payload",{}),

            offer=offer,

            research=research,

            metadata={
                "trigger_id":context.trigger.get("id"),
                "merchant_id":context.merchant.get("id")
                # in case aboveline return none((store manager fetch manager using manager_id) itmight because of id , replace id with merchant_id
            }
        )
    
    
    # ------------------------------------------------------
    # HELPER 1
    # SHOULD USE LLM ?
    # ------------------------------------------------------

    def _should_use_llm(
        self,
        action_type: str
    ) -> bool:
        """
        Decide whether this trigger kind
        requires an LLM.

        Rule based.
        O(1) lookup.
        """

        

        return self.LLM_REQUIRED.get(
            action_type,
            True
        )


    # ------------------------------------------------------
    # HELPER 2
    # SELECT RESEARCH ITEM
    # ------------------------------------------------------

    def _select_research(
        self,
        context: BotContext
    ) -> Optional[Dict[str, Any]]:
        """
        Selects the appropriate research / digest
        item for the current trigger.

        Matching is performed using the
        payload.top_item_id.
        """

        # Only research related triggers
        if context.trigger.get("kind") != "research_digest":
            return None

        payload = context.trigger.get("payload", {})

        target_id = payload.get("top_item_id")

        if target_id is None:
            return None

        digest = context.category.get("digest", [])

        for item in digest:

            if item.get("id") == target_id:
                return item

        return None

    # ------------------------------------------------------
    # HELPER 3
    # SELECT OFFER
    # ------------------------------------------------------

    def _select_offer(
        self,
        context: BotContext
    ) -> Optional[Dict[str, Any]]:
        """
        Select the best offer from the category
        offer catalog.

        Preference is given to offers matching
        the customer's audience.
        """

        offers = context.category.get("offer_catalog", [])

        if not offers:
            return None

        customer = context.customer or {}

        audience = customer.get(
            "lifecycle",
            "new_user"
        )

        # First preference:
        # Offer matching customer lifecycle
        for offer in offers:

            if offer.get("audience") == audience:
                return offer

        # Fallback:
        # Return first available offer
        return offers[0]


