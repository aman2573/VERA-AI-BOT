class ReplyEngine:
    """
    ---------------------------------------------------------
    Reply Engine
    ---------------------------------------------------------

    Converts a validated LLMResponse into the
    final Action expected by the judge.
    """

    ACTION_TITLES = {
        "research_digest": "Weekly Research Digest",

        "regulation_change": "Regulation Update",

        "recall_due": "Service Reminder",

        "perf_dip": "Performance Insight",

        "renewal_due": "Subscription Renewal"
    }

    def __init__(self):
        """Initialize Reply Engine."""
        pass


    # ------------------------------------------------------
    # PUBLIC METHOD
    # ------------------------------------------------------

    def build_action(
        self,
        plan: ExecutionPlan,
        response: LLMResponse
    ) -> Action:
        """
        Convert a validated LLMResponse into
        the final Action expected by the Judge.
        """

        # ------------------------------------------
        # Build message
        # ------------------------------------------

        message = response.body

        if response.cta:

            message += f"\n\n{response.cta}"

        # ------------------------------------------
        # Build title
        # ------------------------------------------

        title = self.ACTION_TITLES.get(

            plan.action_type,

            plan.action_type.replace(
                "_",
                " "
            ).title()

        )

        # ------------------------------------------
        # Return final Action
        # ------------------------------------------

        return Action(

            action_type=plan.action_type,

            title=title,

            message=message,

            priority=plan.priority

        )


