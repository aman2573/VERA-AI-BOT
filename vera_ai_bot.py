# TRIGGER EVALUATOR
# ==========================================================

class TriggerEvaluator: 
    """
    ---------------------------------------------------------
    Trigger Validation Engine
    ---------------------------------------------------------

    Validates whether a trigger should continue through
    Vera's execution pipeline.

    This class NEVER:

    - Generates responses
    - Calls the LLM
    - Modifies StoreManager

    It ONLY validates.
    """

    def __init__(self, store: StoreManager):
        """
        Dependency Injection.

        TriggerEvaluator depends on StoreManager
        for suppression lookups.
        """

        self.store = store

    # ------------------------------------------------------
    # PUBLIC METHOD
    # ------------------------------------------------------

    def evaluate(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Main entry point.

        Runs every validation in order.

        Stops immediately when
        one validation fails.
        """

        result = self._check_trigger(context)
        if not result.should_continue:
            return result

        result = self._check_merchant(context)
        if not result.should_continue:
            return result

        result = self._check_category(context)
        if not result.should_continue:
            return result

        result = self._check_customer(context)
        if not result.should_continue:
            return result

        result = self._check_suppression(context)
        if not result.should_continue:
            return result

        result = self._check_required_context(context)
        if not result.should_continue:
            return result

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=True,
            reason="Trigger validation successful."
        )
    
    # HELPER FUNCTIONS FOR TRIGGER EVALUATOR 
    # CHECK 1
    def _check_trigger(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Ensures the trigger exists.
        """

        if context.trigger is None:

            return TriggerDecision(
                is_valid=False,
                should_continue=False,
                use_llm=False,
                reason="Trigger not found."
            )

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=False,
            reason="Trigger available."
        )

    # CHECK 2
    def _check_merchant(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Ensures merchant exists.
        """

        if context.merchant is None:

            return TriggerDecision(
                is_valid=False,
                should_continue=False,
                use_llm=False,
                reason="Merchant not found."
            )

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=False,
            reason="Merchant available."
        )
    
    # CHECK 3
    def _check_category(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Ensures merchant category exists.
        """

        if context.category is None:

            return TriggerDecision(
                is_valid=False,
                should_continue=False,
                use_llm=False,
                reason="Category not found."
            )

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=False,
            reason="Category available."
        )

    # CHECK 4
    def _check_customer(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Validates customer when required.

        Merchant-scoped triggers do not require
        a customer.
        """

        scope = context.trigger.get("scope")

        if scope == CUSTOMER_SCOPE and context.customer is None:

            return TriggerDecision(
                is_valid=False,
                should_continue=False,
                use_llm=False,
                reason="Customer required but missing."
            )

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=False,
            reason="Customer validation passed."
        )

    # CHECK 5
    def _check_suppression(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Rejects suppressed triggers.
        """

        suppression_key = context.trigger.get("id")

        if (
            suppression_key
            and
            self.store.is_suppressed(suppression_key)
        ):

            return TriggerDecision(
                is_valid=True,
                should_continue=False,
                use_llm=False,
                reason="Trigger currently suppressed."
            )

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=False,
            reason="Suppression check passed."
        )

    # CHECK6 
    def _check_required_context(
        self,
        context: BotContext
    ) -> TriggerDecision:
        """
        Placeholder for future runtime/context
        validation.

        At the moment every trigger passes.
        """

        return TriggerDecision(
            is_valid=True,
            should_continue=True,
            use_llm=False,
            reason="Context validation passed."
        )


